<?php

  $a = 5;
  $b = 3;

  if($a == $b) { //Operador igualdade.
    echo "A é igual a B <br>";
  }

  if($a != $b) { //Operador diferença.
    echo "A é diferente a B <br>";
  }
  
  if($a === $b) { //Operador idêntico.
    echo "A é identico a B <br>";
  }
  
  if($a !== $b) { //Operador não idêntico.
    echo "A não é identico a B <br>";
  }